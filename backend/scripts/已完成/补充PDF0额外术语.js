// 先检查重复
const existing = db.prepare('SELECT 名称 FROM 词条').all().map(r => r.名称);
const duplicates = terms.filter(t => existing.includes(t.名称));
if (duplicates.length > 0) {
  console.log('⚠ 以下术语已存在于数据库中，将跳过:');
  duplicates.forEach(t => console.log('  -', t.名称));
}

const newTerms = terms.filter(t => !existing.includes(t.名称));
console.log(`\n待插入: ${newTerms.length} 条 (跳过 ${duplicates.length} 条重复)`);

// 分配ID并插入
const insert = db.prepare(`
  INSERT INTO 词条 (词条ID, 学科, 名称, 翻译, 本质, 提示, 跨学科别名, 热度, 创建时间)
  VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
`);

const counters = { ...maxIds };
let inserted = 0;
const now = new Date().toISOString();

db.transaction(() => {
  for (const t of newTerms) {
    if (!counters[t.prefix]) counters[t.prefix] = 0;
    counters[t.prefix]++;
    const id = t.prefix + counters[t.prefix];
    insert.run(id, t.学科, t.名称, t.翻译, t.本质, t.提示, t.跨学科别名, 0, now);
    console.log(`  ✓ [${id}] ${t.学科} | ${t.名称} ↔ ${t.翻译}`);
    inserted++;
  }
})();

console.log(`\n✅ 成功插入 ${inserted} 条术语`);

// 统计各学科最终数量
const stats = db.prepare('SELECT 学科, COUNT(*) as count FROM 词条 GROUP BY 学科 ORDER BY count DESC').all();
console.log('\n各学科术语统计:');
let total = 0;
for (const s of stats) {
  console.log(`  ${s.学科}: ${s.count}`);
  total += s.count;
}
console.log(`  总计: ${total}`);

db.close();
