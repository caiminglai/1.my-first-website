const Database = require('better-sqlite3');
const path = require('path');

const dbPath = path.join(__dirname, '..', 'data', '同物异名.db');
const db = new Database(dbPath);

// 查询各学科当前最大编号
const prefixes = {
  '化学': 'ch', '物理学': 'p', '地球科学': 'p',
  '计算机': 'c', '控制论': 'b', '医学心理': 'd', '数学': 'm'
};

const maxIds = {};
for (const disc of Object.keys(new Set(Object.values(prefixes)))) {
  // skip
}
const disciplines = [...new Set(Object.values(prefixes))];
for (const disc of disciplines) {
  const prefix = Object.entries(prefixes).find(([, v]) => v === disc);
  // 查询该前缀的最大编号
  const rows = db.prepare(`SELECT 词条ID FROM 词条 WHERE 词条ID LIKE ?`).all(prefix + '%');
  let maxNum = 0;
  for (const row of rows) {
    const numStr = row.词条ID.slice(prefix.length);
    const num = parseInt(numStr);
    if (!isNaN(num) && num > maxNum) maxNum = num;
  }
  maxIds[prefix] = maxNum;
}

